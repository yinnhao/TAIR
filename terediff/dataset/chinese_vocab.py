# 合并字符集 - ASCII + 中文
MIXED_CTLABELS = ASCII_CHARS + COMMON_CHINESE_CHARS

# 去重并保持顺序
def remove_duplicates_keep_order(seq):
    seen = set()
    return [x for x in seq if not (x in seen or seen.add(x))]

CTLABELS = remove_duplicates_keep_order(MIXED_CTLABELS)

# 创建字符到索引的映射
CHAR_TO_IDX = {char: idx for idx, char in enumerate(CTLABELS)}
IDX_TO_CHAR = {idx: char for idx, char in enumerate(CTLABELS)}

# 词汇表大小
VOCAB_SIZE = len(CTLABELS)
print(f"Total vocabulary size: {VOCAB_SIZE}")

def get_vocab_info():
    """返回词汇表信息"""
    return {
        'vocab_size': VOCAB_SIZE,
        'ctlabels': CTLABELS,
        'char_to_idx': CHAR_TO_IDX,
        'idx_to_char': IDX_TO_CHAR
    } 